class Cat:
    def __init__(self, animal, name, gender, age):
        self.animal = animal
        self.name = name
        self.gender = gender
        self.age = age

    def getAnimal(self):
        return self.animal

    def getName(self):
        return self.name

    def getGender(self):
        return self.gender

    def getAge(self):
        return self.age